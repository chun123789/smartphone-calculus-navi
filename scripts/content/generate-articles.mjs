import fs from "node:fs/promises";
import path from "node:path";
import matter from "gray-matter";
import { fileURLToPath } from "node:url";
import { validateArticlePlan } from "./validate-plan.mjs";
import { getInteractiveFromModule } from "./module-presets.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, "../..");
const ARTICLES_ROOT = path.join(ROOT, "src", "articles");

function sectionLabel(section) {
  return (
    {
      basics: "微積の準備",
      differentiation: "微分",
      integration: "積分",
      fundamental: "微積の基本定理",
      mistakes: "ミス集"
    }[section] ?? section
  );
}

function estimateMinutes(level, track) {
  if (track === "exam") {
    return level === "発展" ? 10 : 8;
  }
  return level === "基礎" ? 6 : level === "標準" ? 8 : 10;
}

function examTag(track) {
  return track === "exam" ? "共通テスト対策" : "定期テスト対策";
}

function misconception(intent, title) {
  if (intent === "mistake") {
    return "記号だけ追って意味を確認しないまま計算を進めてしまう";
  }
  if (intent === "exam") {
    return "誘導文の条件を式に変換する前に計算を始めてしまう";
  }
  return `${title} を公式暗記だけで処理してしまう`;
}

function buildDescription(row) {
  const examTail = [
    "条件から必要式を判断できるようにする。",
    "誘導文のゴールから解答方針を選べるようにする。",
    "設問に沿って必要な式を組み立てられるようにする。"
  ][row.order % 3];

  const regularTail = [
    "要点を短時間で説明できるようにする。",
    "見た瞬間に手順を再現できるようにする。",
    "解法の意図を自分の言葉で言えるようにする。"
  ][row.order % 3];

  const byIntent = {
    definition: `${row.title}を図と式で結び、意味と使いどころを整理して${regularTail}`,
    procedure: `${row.title}で迷う分岐を整理し、条件を見たら次の一手が選べるようにする。`,
    concept: `${row.title}で変化の見方をそろえ、グラフと式の対応を言葉で説明できるようにする。`,
    exam: `${row.title}を共通テスト形式で読み解き、${examTail}`,
    mistake: `${row.title}の誤答パターンを反例で確認し、失点ポイントを自力で修正できるようにする。`,
    review: `${row.title}の頻出ポイントを10分で再接続し、解法の流れを短時間で思い出せるようにする。`
  };
  return byIntent[row.intent] ?? `${row.title}を図と式でつなぎ、要点を短時間で説明できるようにする。`;
}

function isNonEmptyString(value) {
  return typeof value === "string" && value.trim().length > 0;
}

function isPlaceholderDescription(value) {
  return /高校生向けにスマホで理解する。?$/.test(String(value ?? "").trim());
}

function isLikelyAutoGeneratedDescription(value) {
  const text = String(value ?? "").trim();
  return /(図と式|共通テスト形式|誤答パターン|頻出ポイント).+できるようにする。?$/.test(text);
}

function isQualityDescription(value) {
  const text = String(value ?? "").trim();
  if (text.length < 40 || text.length > 75) {
    return false;
  }
  return /(できる|身につく|理解|説明|判断|見抜|再現)/.test(text);
}

function normalizedStringArray(value, expectedLength) {
  if (!Array.isArray(value)) {
    return null;
  }
  const cleaned = value
    .map((item) => String(item ?? "").trim())
    .filter(Boolean);
  if (cleaned.length !== expectedLength) {
    return null;
  }
  return cleaned;
}

function pickString(existingValue, fallback, validator = isNonEmptyString) {
  if (validator(existingValue)) {
    return String(existingValue).trim();
  }
  return fallback;
}

function makeBody(row) {
  return [
    `## 導入`,
    "",
    `${row.title}は、式だけでなく図の意味を同時に見ると理解が安定します。`,
    "",
    "## 誤答例 → 反例", 
    "",
    `- ${misconception(row.intent, row.title)}`,
    "- スライダーで条件を変え、誤答が成立しないケースを確認する",
    "",
    "## 正ルール", 
    "",
    "1. 条件を先に日本語で整理する",
    "2. 式とグラフを同じタイミングで確認する",
    "3. 最後に符号・範囲・単位を点検する",
    "",
    "## 確認問題", 
    "",
    "- 30秒で要点を説明できるか",
    "- 同型問題で手順を再現できるか"
  ].join("\n");
}

function buildFrontmatter(row) {
  const interactive = getInteractiveFromModule(row.vizModule);
  const commonTags = [sectionLabel(row.section), row.track === "exam" ? "共通テスト" : "定期テスト", row.intent];
  return {
    slug: row.slug,
    title: row.title,
    description: buildDescription(row),
    section: row.section,
    order: row.order,
    track: row.track,
    intent: row.intent,
    level: row.level,
    priority: row.priority,
    estimatedMinutes: estimateMinutes(row.level, row.track),
    examTag: examTag(row.track),
    misconceptionPattern: misconception(row.intent, row.title),
    cta: row.track === "exam" ? "共通テスト演習へ進む" : "次の基礎記事へ進む",
    hookQuestion: row.hookQuestion,
    oneLineAnswer: row.oneLineAnswer,
    keyTakeaways: [row.takeaway1, row.takeaway2, row.takeaway3],
    checkpointQuestions: [row.check1, row.check2],
    tags: commonTags,
    interactive,
    links: {
      prerequisitesCandidates: [row.prereq1, row.prereq2],
      nextStep: row.next,
      mistakes: row.mistake
    },
    updates: [
      {
        date: row.published,
        note: "構成を最新方針に更新"
      }
    ],
    published: row.published,
    conclusion: `${row.title}は「誤答→反例→正ルール」で理解すると定着が速い。`,
    example: "本文のルールを使って、1問だけ自力で再現してみよう。",
    commonMistake: misconception(row.intent, row.title)
  };
}

function mergeFrontmatter(existing, generated) {
  const merged = {
    ...generated,
    title: pickString(existing.title, generated.title),
    description:
      isQualityDescription(existing.description) &&
      !isPlaceholderDescription(existing.description) &&
      !isLikelyAutoGeneratedDescription(existing.description)
      ? String(existing.description).trim()
      : generated.description,
    estimatedMinutes:
      Number.isInteger(existing.estimatedMinutes) && existing.estimatedMinutes > 0
        ? existing.estimatedMinutes
        : generated.estimatedMinutes,
    examTag: pickString(existing.examTag, generated.examTag),
    misconceptionPattern: pickString(existing.misconceptionPattern, generated.misconceptionPattern),
    cta: pickString(existing.cta, generated.cta),
    hookQuestion: pickString(existing.hookQuestion, generated.hookQuestion),
    oneLineAnswer: pickString(existing.oneLineAnswer, generated.oneLineAnswer),
    tags: normalizedStringArray(existing.tags, 3) ?? generated.tags,
    keyTakeaways: normalizedStringArray(existing.keyTakeaways, 3) ?? generated.keyTakeaways,
    checkpointQuestions: normalizedStringArray(existing.checkpointQuestions, 2) ?? generated.checkpointQuestions,
    conclusion: pickString(existing.conclusion, generated.conclusion),
    example: pickString(existing.example, generated.example),
    commonMistake: pickString(existing.commonMistake, generated.commonMistake)
  };

  if (Array.isArray(existing.updates) && existing.updates.length > 0) {
    merged.updates = existing.updates;
  }

  return merged;
}

export async function generateArticlesFromPlan() {
  const rows = await validateArticlePlan();

  for (const row of rows) {
    const sectionDir = path.join(ARTICLES_ROOT, row.section);
    const articlePath = path.join(sectionDir, `${row.slug}.md`);
    await fs.mkdir(sectionDir, { recursive: true });

    let body = "";
    let existingFrontmatter = {};
    try {
      const current = await fs.readFile(articlePath, "utf8");
      const parsed = matter(current);
      body = parsed.content.trim();
      if (parsed.data && typeof parsed.data === "object") {
        existingFrontmatter = parsed.data;
      }
    } catch {
      body = "";
      existingFrontmatter = {};
    }

    const generatedFrontmatter = buildFrontmatter(row);
    const frontmatter = mergeFrontmatter(existingFrontmatter, generatedFrontmatter);
    const contentBody = body.length > 0 ? body : makeBody(row);
    const output = matter.stringify(`${contentBody}\n`, frontmatter);
    await fs.writeFile(articlePath, output, "utf8");
  }

  return rows.length;
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const count = await generateArticlesFromPlan();
  console.log(`Generated/updated ${count} articles from plan.`);
}
